# и целые числа N1 и N2 — две посланные команды. Вывести
# ориентацию локатора после выполнения этих команд.
def move(m, d):
    if m == '1':
        d = 3 if d == 0 else d - 1
    elif m == '-1':
        d = 0 if d == 3 else d + 1
    elif m == '2':
        d = (d + 2) % 4
    return d


def dToInt(dt):
    a1 = ['С', 'В', 'Ю', 'З']
    i = 0
    for d1 in a1:
        if dt == d1:
            return i
        i += 1
    return 0

