# 17.Дана строка. Если она представляет собой запись целого числа, то вывести 1,
# если вещественного (с дробной частью) — вывести 2;
# если строку нельзя преобразовать в число, то вывести 0.
# Считать, что дробная часть вещественного числа отделяется от его целой части десятичной точкой «.».
s = input('Введите строку: ')
count = 0
count_dot = 0
for i in range(0, len(s)):
    if ord(s[i]) > 57 or ord(s[i]) < 48:
        count += 1
    elif ord(s[i]) == 46:
        count_dot += 1
if count == 0:
    print(1)
elif count_dot == 1 and count == 0:
    print(2)
else:
    print(0)


