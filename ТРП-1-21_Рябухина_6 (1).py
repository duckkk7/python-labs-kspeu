# 19.Дано целое число N (> 0) и строка S. Преобразовать строку S в строку длины N следующим образом:
# если длина строки S больше  N, то отбросить первые символы, если длина строки S меньше N, то
# в ее начало добавить символы «.» (точка).
n = int(input('Введите N: '))
s = input('Введите строку: ')
if len(s) > n:
    print(s[len(s)-n::])
elif len(s) < n:
    print('.'*(n - len(s)) + s)
else:
    print(s)


